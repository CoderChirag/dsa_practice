class Solution
{
    //Function to find the maximum profit and the number of jobs done.
    int[] JobScheduling(Job arr[], int n)
    {
        // Your code here
        Arrays.sort(arr, (j1, j2)-> Integer.compare(j1.deadline, j2.deadline));
        PriorityQueue<Job> q = new PriorityQueue<>((j1, j2) -> Integer.compare(j2.profit, j1.profit));
        int res[] = new int[2];
        for(int i=arr.length-1; i>=0; i--){
            int slots = 0;
            if(i == 0) slots = arr[i].deadline;
            else slots = arr[i].deadline - arr[i-1].deadline;
            
            q.offer(arr[i]);
            while(slots > 0 && q.size() > 0){
                res[0]++;
                res[1] += q.poll().profit;
                slots--;
            }
        }
        return res;
    }
}

